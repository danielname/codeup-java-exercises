public class StringExercise {
    public static void main(String[] args) {
        String dnEducation = "We don't need no education\n" +
                "We don't need no thought control";
        System.out.println(dnEducation);
        String dnEscapeChars = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(dnEscapeChars);
        String dnC = "In windows, the main drive is usually C:\\";
        System.out.println(dnC);
        String dnBackslash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(dnBackslash);
    }
}
